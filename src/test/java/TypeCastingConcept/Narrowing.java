package TypeCastingConcept;

public class Narrowing {
	
	//Converting bigger Data type to smaller is known an Narrowing. In this, Data loss would be there and it is explicit in nature
	
	public static void main(String[] args) {
		double d = 5.5;
		int i = (int)d;
		
		System.out.println(i);
	}
	
			

}
